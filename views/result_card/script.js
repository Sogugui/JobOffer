function visualiza_primero() {
    document.getElementById('fav-black').style.visibility='visible';
    document.getElementById('fav-black').style.display='block';
    document.getElementById('fav-red').style.visibility='hidden';
    document.getElementById('fav-red').style.display='none';
 };

 function visualiza_segundo() {
    document.getElementById('fav-red').style.visibility='visible';
    document.getElementById('fav-red').style.display='block';
    document.getElementById('fav-black').style.visibility='hidden';
    document.getElementById('fav-black').style.display='none';
 };



